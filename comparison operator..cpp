#include<iostream>//header file
using namespace std;//library file 

int main()//main function 
{//opening bracket 
int x=2,y=3;//declaring and assigning 
cout<<(x==y);//using operator is equal
cout<<(x<y);//using operator less than 
cout<<(x>y);//using operator greater than 
cout<<(x<=y);//using operator less than and equal
cout<<(x>=y);//using operator greater than or equal 
cout<<(x!=y);//using operator not equal 
    return 0;
}//closing bracket